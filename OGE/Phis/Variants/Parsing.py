def parsing_var1():
    url = 'https://phys-oge.sdamgia.ru/test?id=3018827'
    response = requests.get(url)
    soup = BeautifulSoup(response.text, 'lxml')
    quotes = soup.find_all('div', class_='nobreak')
    quotes_text = []
    for quote in quotes:
        quotes_text.append(quote.text)
    return quotes_text


